public class JavaVariables7
{
	public static void main(String[] args)
	{
		double number1= 12.3;
		double number2= 5.5;
		double plus= number1 + number2;
		double substract= number1- number2;
		double multiply= number1 * number2;
		double divide= number1 / number2;
		double nesto1= number1 % number2;
		double nesto2= number2 % number1;
		double substract2= number2- number1;
		System.out.println("number1 + number2 =" + plus);
		System.out.println("number1 - number2 =" + substract);
		System.out.println("number1 * number2 =" + multiply);
		System.out.println("number1 / number2 =" + divide);
		System.out.println("number1 % number2 =" + nesto1);
		System.out.println("number2 % number2 =" + nesto2);
		System.out.println("number2 - number1 =" + substract2);
	}
}
