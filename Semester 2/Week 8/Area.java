public class Area
{
	public static double circles(double radius)
	{
		return 3.1415 * radius * radius;
	}
	public static double rectangles(double width, double length)
	{
		return width * length;
	}
	public static double cylinders(double radius, double height)
	{
		return 3.1415 * radius * radius * height;
	}
}